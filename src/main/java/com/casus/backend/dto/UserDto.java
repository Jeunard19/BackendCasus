package com.casus.backend.dto;

import com.casus.backend.model.User;

public class UserDto {

	private Long id;

	private String name;

	private String email;

	private String username;

	private String password;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public UserDto() {

	}

	public UserDto(Long id, String name, String email, String username) {
		super();
		this.id = id;
		this.name = name;
		this.email = email;
		this.username = username;
	}

	public static final UserDto buildFromUser(User user) {
		return new UserDto(user.getId(), user.getName(), user.getEmail(), user.getUsername());
	}

}
